-- ==========================================
-- 修复因旧版程序导致的转盘热度只加了 1 分的问题
-- 恢复他们原本应得的 +10 分奖励
-- ==========================================

-- 由于旧版错误每次只加了 1 分，所以 hot_score 除以 10 的余数 (hot_score % 10) 
-- 刚好等于该商家被转盘命中但只加了 1 分的「次数」。
-- 我们每次需要补偿 9 分。总补偿分数 = 9 * 命中次数。
-- 运行后，所有非整数倍 10 的热度分数将被完美复原至正确的十位数级别。

UPDATE public.restaurants
SET hot_score = hot_score + 9 * (hot_score % 10)
WHERE hot_score % 10 != 0;
